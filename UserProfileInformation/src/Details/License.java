public class License {
    
    public String licNumber;
    public String licLocation;
    public String licStatus;
    public String licStartDate;
    public String licDueDate;

    public String getLicNumber() {
        return licNumber;
    }

    public void setLicNumber(String licNumber) {
        this.licNumber = licNumber;
    }

    public String getLicLocation() {
        return licLocation;
    }

    public void setLicLocation(String licLocation) {
        this.licLocation = licLocation;
    }

    public String getLicStatus() {
        return licStatus;
    }

    public void setLicStatus(String licStatus) {
        this.licStatus = licStatus;
    }

    public String getLicStartDate() {
        return licStartDate;
    }

    public void setLicStartDate(String licStartDate) {
        this.licStartDate = licStartDate;
    }

    public String getLicDueDate() {
        return licDueDate;
    }

    public void setLicDueDate(String licDueDate) {
        this.licDueDate = licDueDate;
    }
    
    
    
    
    
}
