public void populateTable(){
     DefaultTableModel model = (DefaultTableModel) tblManageVaccine.getModel();

        model.setRowCount(0);

        for (Vaccine vaccine : organization.getVaccineDirectory().getVaccineList()) {
                Object row[] = new Object[3];
                row[0] = vaccine;
                row[1] = vaccine.getDiseaseName();
                row[2] = vaccine.getVaccineqty();
                ((DefaultTableModel) tblManageVaccine.getModel()).addRow(row);
        }
}
