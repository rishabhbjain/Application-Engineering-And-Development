    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        // TODO add your handling code here:
         String disease = txtDisease.getText();
        String vaccine = txtVaccine.getText();
        String vaccineqty = txtvaccineqty.getText();
        if((disease != null)&& !(disease.isEmpty())
           && (vaccine != null)&& !(vaccine.isEmpty()) 
           && (vaccineqty != null)&& !(vaccineqty.isEmpty())
           
            )
        {
            
          try {
            Pattern pattern = Pattern.compile("[^A-Za-z0-9]");
            Matcher m = pattern.matcher(disease + vaccine + vaccineqty);
            boolean boo = m.find();
            if(boo == true || disease == null || vaccine == null || vaccineqty == null) {
            JOptionPane.showMessageDialog(null,"Please remove special character");
        }
            else{
                
                organization.getVaccineDirectory().addVaccine(vaccine, disease,Integer.parseInt(vaccineqty));
                 populateTable();
            JOptionPane.showMessageDialog(null,"Person successfully added");
            }
          }catch(Exception e) {
        JOptionPane.showMessageDialog(null,"Please Enter Numeric Data in SSN ");
        }
        }
        else{
        JOptionPane.showMessageDialog(null,"Please Enter the Data ");

    }
    }//GEN-LAST:event_btnEnterActionPerformed


